/* Adventures in JavaScript
 * Listing 4.07
 * Using the findTotal function to display a calculation
 */

var number1 = 1000;
var number2 = 66;
var result;

var findTotal = function () {
    result = number1 + number2;
};

findTotal();

console.log(number1 + " + " + number2 + " = " + result);



